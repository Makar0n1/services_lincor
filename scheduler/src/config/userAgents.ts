// Функция для получения случайного User-Agent
export function getRandomUserAgent(): string {
  const randomIndex = Math.floor(Math.random() * USER_AGENTS.length);
  return USER_AGENTS[randomIndex]!;
}

// Функция для получения User-Agent по индексу (для детерминированной ротации)
export function getUserAgentByIndex(index: number): string {
  return USER_AGENTS[index % USER_AGENTS.length]!;
}
